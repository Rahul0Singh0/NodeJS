// Global Variables

// __dirname
console.log(__dirname); // It will print the directory path of the current file

// module
console.log(module); // It will print the module object

console.log(__filename); // It will print the file path of the current file

// process
// Environment variables can be accessed through process.env
console.log(process); // It will print the process object
console.log(process.env); // It will print the environment variables

console.log(require); // It will print the require function

console.log(global); // setTimeout() etc..